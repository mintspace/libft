#include "libft.h"

int		ft_wdcount(char const *s, char c)
{
	int	w;

	w = 0;
	while (*s != '\0')
	{
		if (*s != c)
		{
			while (*s != c && *s != '\0')
				s++;
			w++;
		}
		if (*s != '\0')
			s++;
	}
	return (w);
}
